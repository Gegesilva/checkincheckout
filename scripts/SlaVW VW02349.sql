

ALTER VIEW [dbo].[VW02349]
AS

SELECT 
    T.TB02115_CODIGO AS os,

    CASE 
        WHEN T.TB02115_PREVENTIVA = 'I' THEN 'Instalaçăo' 
        WHEN T.TB02115_PREVENTIVA = 'N' THEN 'Normal' 
        WHEN T.TB02115_PREVENTIVA = 'D' THEN 'Desinstalaçăo' 
        WHEN T.TB02115_PREVENTIVA = 'P' THEN 'Preventiva' 
        WHEN T.TB02115_PREVENTIVA = 'R' THEN 'Retorno / Recarga' 
        ELSE 'Aferiçăo' 
    END AS tipo,

    CASE 
        WHEN T.TB02115_ORIGEM = 'C' THEN CONTR.TB02111_TIPOCONTR
        WHEN T.TB02115_ORIGEM = 'B' THEN 'B'
        ELSE 'I'
    END AS tipocontrato,

    T.TB02115_CODEMP AS codemp,
    EMP.TB01007_NOME AS empresa,

    T.TB02115_DATA AS data,

    CONVERT(VARCHAR(10), T.TB02115_DATA, 103) AS datab,

    SUBSTRING(CONVERT(VARCHAR(10),T.TB02115_DATA,103),7,4) + '-' +
    SUBSTRING(CONVERT(VARCHAR(10),T.TB02115_DATA,103),4,2) AS anomes,

    SUBSTRING(CONVERT(VARCHAR(10),T.TB02115_DTFECHA,103),7,4) + '-' +
    SUBSTRING(CONVERT(VARCHAR(10),T.TB02115_DTFECHA,103),4,2) AS anomesfechamento,

    T.TB02115_CODCLI AS codcli,

    CASE 
        WHEN T.TB02115_ORIGEM IN ('C','B') THEN CLI.TB01008_NOME
        ELSE 'EQUIPAMENTO INTERNO'
    END AS cliente,

    T.TB02115_CONTRATO AS contrato,

    CONTR.TB02111_NOME AS classificacao,

    T.TB02115_NUMSERIE AS serie,

    ISNULL(C.TB02112_PAT, TB02054.TB02054_PAT) AS patrimonio,

    PROD.TB01010_NOME AS equipamento,

    T.TB02115_CODTEC AS codtec,

    ISNULL(
        CASE 
            WHEN T.TB02115_TIPOINTERV = 'I' THEN TEC.TB01024_NOME 
            ELSE PREST.TB01017_NOME 
        END,
        TEC.TB01024_NOME
    ) AS tecnico,

    SLA.CALC_PREVISAO AS sla,
    SLA.CALC_RESTANTE AS previsao,

    CASE WHEN SLA.CALC_RESTANTE < 0 THEN 1 ELSE 0 END AS slaexcedido,

    CAST(T.TB02115_DTFECHA AS DATE) AS datafechamento,

    ST.TB01073_NOME AS status,

    C.TB02112_HORAS AS slacontrato,

    CASE WHEN T.TB02115_REINCIDENCIA = 'S' THEN 1 ELSE 0 END AS reincidencia,

    CASE 
        WHEN DATEDIFF(D,
            (SELECT TOP 1 T2.TB02115_DATA
             FROM TB02115 T2
             WHERE T2.TB02115_NUMSERIE = T.TB02115_NUMSERIE
               AND CAST(T2.TB02115_CODIGO AS INT) < CAST(T.TB02115_CODIGO AS INT)
             ORDER BY T2.TB02115_DATA DESC),
            T.TB02115_DATA
        ) >= 30 
    THEN 0 ELSE 1 END AS retorno,

    FECH.TB02122_CODIGO AS codfech,

    CASE 
        WHEN T.TB02115_ORIGEM = 'C' THEN C.TB02112_BAIRRO 
        ELSE EMPEND.TB00012_BAIRRO 
    END AS bairro,

    CASE 
        WHEN T.TB02115_ORIGEM = 'C' THEN C.TB02112_CIDADE 
        ELSE EMPEND.TB00012_CIDADE 
    END AS cidade,

    CASE 
        WHEN T.TB02115_ORIGEM = 'C' THEN C.TB02112_ESTADO 
        ELSE EMPEND.TB00012_ESTADO 
    END AS estado,

    CASE 
        WHEN T.TB02115_ORIGEM = 'C' THEN C.TB02112_CEP 
        ELSE EMPEND.TB00012_CEP 
    END AS cep,

    SITE.TB02176_NOME AS site,
    DEPTO.TB02177_NOME AS departamento,

    (
        SELECT TOP 1 TB01048_NOME 
        FROM TB01048 
        LEFT JOIN TB02116 ON TB02116_DEFEITO = TB01048_CODIGO 
        WHERE TB02116_CODIGO = T.TB02115_CODIGO
    ) AS defeito,

    COND.TB01055_NOME AS condintervencao,

    1 AS qtde,

    CAST(TEC.TB01024_OBS AS VARCHAR(20)) AS equipe,

    TB02115_STATUS codstatus,
    TB02115_NUMSERIE Serial

FROM TB02115 T

LEFT JOIN TB02112 C 
    ON T.TB02115_CONTRATO = C.TB02112_CODIGO
   AND T.TB02115_PRODUTO = C.TB02112_PRODUTO
   AND T.TB02115_NUMSERIE = C.TB02112_NUMSERIE

LEFT JOIN TB02054 
    ON TB02054.TB02054_CODEMP = T.TB02115_CODEMP
   AND TB02054.TB02054_NUMSERIE = T.TB02115_NUMSERIE
   AND TB02054.TB02054_PRODUTO = T.TB02115_PRODUTO

LEFT JOIN TB01008 CLI ON CLI.TB01008_CODIGO = T.TB02115_CODCLI
LEFT JOIN TB01010 PROD ON PROD.TB01010_CODIGO = T.TB02115_PRODUTO
LEFT JOIN TB01024 TEC ON TEC.TB01024_CODIGO = T.TB02115_CODTEC
LEFT JOIN TB01017 PREST ON PREST.TB01017_CODIGO = T.TB02115_PREST
LEFT JOIN TB01007 EMP ON EMP.TB01007_CODIGO = T.TB02115_CODEMP

LEFT JOIN TB00012 EMPEND 
    ON EMPEND.TB00012_CODIGO = T.TB02115_CODEMP
   AND EMPEND.TB00012_TABELA = 'TB01007'
   AND EMPEND.TB00012_TIPO = '01'

LEFT JOIN TB02111 CONTR ON CONTR.TB02111_CODIGO = T.TB02115_CONTRATO
LEFT JOIN TB02122 FECH ON FECH.TB02122_NUMOS = T.TB02115_CODIGO
LEFT JOIN TB01055 COND ON COND.TB01055_CODIGO = FECH.TB02122_CONDICAO
LEFT JOIN TB01073 ST ON ST.TB01073_CODIGO = T.TB02115_STATUS
LEFT JOIN TB02176 SITE ON SITE.TB02176_CODIGO = T.TB02115_CODSITE
LEFT JOIN TB02177 DEPTO ON DEPTO.TB02177_CODIGO = T.TB02115_CODDEPTO

OUTER APPLY dbo.FT02020(
    T.TB02115_CODIGO,
    T.TB02115_PREVENTIVA,
    T.TB02115_DATA,
    T.TB02115_DTFECHA,
    CLI.TB01008_HORAS,
    CONTR.TB02111_TIPOCONTR,
    CONTR.TB02111_CONTROLESLA,
    C.TB02112_HORAS,
    C.TB02112_TIPOHORAS,
    C.TB02112_TIPOSLA,
    C.TB02112_CODSITE,
    'A'
) SLA

WHERE 
    T.TB02115_SITUACAO = 'A'
    AND T.TB02115_DTFECHA IS NULL
GO


